package com.gestaoequipamentos.bean;

public class PecasAprovacaoOSBean {
	private String id;
	private String partNo;
	private String partName;
	private String qtd;
	private String uniteRate;
	private String dnPrice;
	private String uniteCl;
	private String clPrice;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPartNo() {
		return partNo;
	}
	public void setPartNo(String partNo) {
		this.partNo = partNo;
	}
	public String getPartName() {
		return partName;
	}
	public void setPartName(String partName) {
		this.partName = partName;
	}
	public String getQtd() {
		return qtd;
	}
	public void setQtd(String qtd) {
		this.qtd = qtd;
	}
	public String getUniteRate() {
		return uniteRate;
	}
	public void setUniteRate(String uniteRate) {
		this.uniteRate = uniteRate;
	}
	public String getDnPrice() {
		return dnPrice;
	}
	public void setDnPrice(String dnPrice) {
		this.dnPrice = dnPrice;
	}
	public String getUniteCl() {
		return uniteCl;
	}
	public void setUniteCl(String uniteCl) {
		this.uniteCl = uniteCl;
	}
	public String getClPrice() {
		return clPrice;
	}
	public void setClPrice(String clPrice) {
		this.clPrice = clPrice;
	}
}
